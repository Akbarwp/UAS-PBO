    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            conn = KoneksiQuiz.getKoneksi();
            
            cmd = conn.createStatement();
            rs = cmd.executeQuery("SELECT * FROM Quiz1 WHERE ISBN = '" + txISBN.getText() + "'");
            
            //Setting kolom dari DefaultTableModel
            mTablePerpus = new DefaultTableModel(new String[] {"JUDUL", "TAHUN TERBIT", "PENGARANG", "JENIS BUKU", "LOKASI RAK", "KATEGORI", "ISBN"},0);
            
            //Pindah isi dari result set ke dalam DefaultTableModel
            if (rs.next()) {
                do {                    
                    mTablePerpus.addRow(new Object[] {rs.getString("Judul"), rs.getInt("Tahun_terbit"), rs.getString("Pengarang"), 
                        rs.getString("Jenis_buku"), rs.getString("Lokasi"), rs.getString("Kategori"), rs.getString("ISBN")});
                } while (rs.next());
                
            } else {
                System.out.println("Data tidak ada");
            }
            TablePerpus.setDefaultEditor(Object.class, null);
            TablePerpus.setModel(mTablePerpus);
            
        } catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void TablePerpusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablePerpusMouseClicked
        // TODO add your handling code here:
        int baris = TablePerpus.rowAtPoint(evt.getPoint());
        if (evt.getClickCount() == 2){
            String Judul = TablePerpus.getValueAt(baris, 0).toString();
            txJudul.setText(Judul);
            String Tahun = TablePerpus.getValueAt(baris, 1).toString();
            txTahun.setText(Tahun);
            String Pengarang = TablePerpus.getValueAt(baris, 2).toString();
            txPengarang.setText(Pengarang);
            String Jenis = TablePerpus.getValueAt(baris, 3).toString();
            cbJenis.setSelectedItem(Jenis);
            String Lokasi = TablePerpus.getValueAt(baris, 4).toString();
            if(Lokasi.equalsIgnoreCase("Rak 1")) {
                rbRak1.setSelected(true);
            } else if(Lokasi.equalsIgnoreCase("Rak 2")) {
                rbRak2.setSelected(true);
            } else if(Lokasi.equalsIgnoreCase("Rak 3")) {
                rbRak3.setSelected(true);
            }
            String Kategori = TablePerpus.getValueAt(baris, 5).toString();
            if(Kategori.equalsIgnoreCase("Teknologi Bisnis Bahasa Sejarah")) {
                cbTeknologi.setSelected(true);
                cbBisnis.setSelected(true);
                cbBahasa.setSelected(true);
                cbSejarah.setSelected(true);
            }
            
            
            if(Kategori.equalsIgnoreCase("Teknologi Bisnis Bahasa ")) {
                cbTeknologi.setSelected(true);
                cbBisnis.setSelected(true);
                cbBahasa.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Teknologi Bisnis Sejarah")) {
                cbTeknologi.setSelected(true);
                cbBisnis.setSelected(true);
                cbSejarah.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Teknologi Bahasa Sejarah")) {
                cbTeknologi.setSelected(true);
                cbBahasa.setSelected(true);
                cbSejarah.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Bisnis Bahasa Sejarah")) {
                cbBisnis.setSelected(true);
                cbBahasa.setSelected(true);
                cbSejarah.setSelected(true);
            }
            
            // Teknologi Bisnis Bahasa Sejarah
            if(Kategori.equalsIgnoreCase("Teknologi Bisnis ")) {
                cbTeknologi.setSelected(true);
                cbBisnis.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Teknologi Bahasa ")) {
                cbTeknologi.setSelected(true);
                cbBahasa.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Teknologi Sejarah")) {
                cbTeknologi.setSelected(true);
                cbSejarah.setSelected(true);
            }
            
            if(Kategori.equalsIgnoreCase("Bisnis Bahasa ")) {
                cbBisnis.setSelected(true);
                cbBahasa.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Bisnis Sejarah")) {
                cbBisnis.setSelected(true);
                cbSejarah.setSelected(true);
            }
            
            if(Kategori.equalsIgnoreCase("Bahasa Sejarah")) {
                cbBahasa.setSelected(true);
                cbSejarah.setSelected(true);
            }
            
            
            if(Kategori.equalsIgnoreCase("Teknologi ")) {
                cbTeknologi.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Bisnis ")) {
                cbBisnis.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Bahasa ")) {
                cbBahasa.setSelected(true);
            }
            if(Kategori.equalsIgnoreCase("Sejarah")) {
                cbSejarah.setSelected(true);
            }

            String ISBN = TablePerpus.getValueAt(baris, 6).toString();
            txISBN.setText(ISBN);
        }
    }//GEN-LAST:event_TablePerpusMouseClicked

