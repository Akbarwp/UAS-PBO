    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            
            String kategori = "";
            if(cbTeknologi.isSelected()) {
                kategori = "Teknologi " + kategori;
            }
            if(cbBisnis.isSelected()) {
                kategori = kategori + "Bisnis ";
            }
            if(cbBahasa.isSelected()) {
                kategori = kategori + "Bahasa ";
            }
            if(cbSejarah.isSelected()) {
                kategori = kategori + "Sejarah";
            }
            
            String rak = null;
            if(rbRak1.isSelected()) {
                rak = "Rak 1";
            } else if(rbRak2.isSelected()) {
                rak = "Rak 2";
            } else if(rbRak3.isSelected()) {
                rak = "Rak 3";
            }
            
            conn = KoneksiQuiz.getKoneksi();
            SQL = "INSERT INTO Quiz1 (Judul, Tahun_terbit, Pengarang, Jenis_buku, Lokasi, Kategori, ISBN) VALUES"
                    + "('" + txJudul.getText() + "',"
                    + txTahun.getText() + ","
                    + "'" + txPengarang.getText() + "',"
                    + "'" + cbJenis.getSelectedItem().toString() + "',"
                    + "'" + rak + "',"
                    + "'" + kategori + "',"
                    + "'" + txISBN.getText() + "')";
            
            cmd = conn.createStatement();
            cmd.executeUpdate(SQL);
            JOptionPane.showMessageDialog(this, "Success");

            refresh();
            showTabel();
            
        } catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int jawab = JOptionPane.showConfirmDialog(this, "Silakan Konfirmasi?");
            
            switch(jawab){
                case JOptionPane.YES_OPTION: 
                    conn = KoneksiQuiz.getKoneksi();
                    SQL = "DELETE FROM Quiz1 WHERE ISBN = '" + txISBN.getText() + "'";
                    cmd = conn.createStatement();
                    cmd.executeUpdate(SQL);
                    JOptionPane.showMessageDialog(this, "Success");
                    showTabel();
                    refresh();
                    break;
                    
                case JOptionPane.NO_OPTION:
                    break;
                    
                case JOptionPane.CANCEL_OPTION:
                    break;
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

