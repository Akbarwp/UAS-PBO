    private void showTabel() {
        try {
            conn = KoneksiQuiz.getKoneksi();
            
            cmd = conn.createStatement();
            rs = cmd.executeQuery("SELECT * FROM Quiz1");
            
            //Setting kolom dari DefaultTableModel
            mTablePerpus = new DefaultTableModel(new String[] {"JUDUL", "TAHUN TERBIT", "PENGARANG", "JENIS BUKU", "LOKASI RAK", "KATEGORI", "ISBN"},0);
            
            //Pindah isi dari result set ke dalam DefaultTableModel
            if (rs.next()) {
                do {                    
                    mTablePerpus.addRow(new Object[] {rs.getString("Judul"), rs.getInt("Tahun_terbit"), rs.getString("Pengarang"), 
                        rs.getString("Jenis_buku"), rs.getString("Lokasi"), rs.getString("Kategori"), rs.getString("ISBN")});
                } while (rs.next());
                
            } /* else {
                System.out.println("Data tidak ada");
            }*/
            TablePerpus.setDefaultEditor(Object.class, null);
            TablePerpus.setModel(mTablePerpus);
            
        } catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
        }
    }
    
