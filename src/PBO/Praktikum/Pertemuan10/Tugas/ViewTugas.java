    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if(rbLaki.isSelected()) {
            gender = "Laki-laki";
        } else {
            gender = "Perempuan";
        }
        
        ct.insertTugas(
                txNim.getText(),
                txNama.getText(),
                txLahir.getText(),
                gender,
                cbJurusan.getSelectedItem().toString()
        );
        JOptionPane.showMessageDialog(this, "Data Berhasil disimpan");
        tampilData();
    }//GEN-LAST:event_btSimpanActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        if(rbLaki.isSelected()) {
            gender = "Laki-laki";
        } else {
            gender = "Perempuan";
        }
        
        ct.updateTugas(
                tempRowSelected,
                txNim.getText(),
                txNama.getText(),
                txLahir.getText(),
                gender,
                cbJurusan.getSelectedItem().toString()
        );
        JOptionPane.showMessageDialog(this, "Data Berhasil diperbarui");
        tampilData();
    }//GEN-LAST:event_btUpdateActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        ct.deleteTugas(tempRowSelected);
        JOptionPane.showMessageDialog(this, "Data Berhasil dihapus");
        tampilData();
    }//GEN-LAST:event_btDeleteActionPerformed

    private void tabelTugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelTugasMouseClicked
        // TODO add your handling code here:
        tempRowSelected = tabelTugas.getSelectedRow();
        if(evt.getClickCount() == 2) {
            txNim.setText(ct.getListTugas().get(tabelTugas.getSelectedRow()).getNim());
            txNama.setText(ct.getListTugas().get(tabelTugas.getSelectedRow()).getNama());
            txLahir.setText(ct.getListTugas().get(tabelTugas.getSelectedRow()).getLahir());
            if(ct.getListTugas().get(tabelTugas.getSelectedRow()).getGender().equals("Laki-laki")) {
                rbLaki.setSelected(true);
            } else {
                rbPerempuan.setSelected(true);
            }
            cbJurusan.setSelectedItem(ct.getListTugas().get(tabelTugas.getSelectedRow()).getJurusan());
        }
    }//GEN-LAST:event_tabelTugasMouseClicked

