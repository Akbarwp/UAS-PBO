    private void tampilData() {
        String[] kolom = {"NIM", "Nama", "Tgl Lahir", "Jenis Kelamin", "Jurusan"};
        Object[][] data = new Object[ct.getArraySize()][5];
        
        int i = 0;
        while(i < ct.getArraySize()) {
            String ArrayData[] = {
                ct.getListTugas().get(i).getNim(),
                ct.getListTugas().get(i).getNama(),
                ct.getListTugas().get(i).getLahir(),
                ct.getListTugas().get(i).getGender(),
                ct.getListTugas().get(i).getJurusan()
            };
            
            data[i] = ArrayData;
            i++;
        }
        
        tabelModel = new DefaultTableModel(data, kolom){
            @Override
            public boolean isCellEditable(int rowIndex, int collIndex){
                return false;
            }
        };
        
        tabelTugas.setModel(tabelModel);
        reset();
    }

