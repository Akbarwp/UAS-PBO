package PBO.Praktikum.Pertemuan6;

public class RumusBalok {
    
    private double panjang, lebar, tinggi;
    
    public RumusBalok(double p, double l, double t) {
        panjang = p;
        lebar = l;
        tinggi = t;
    }
    
    double getPanjang() {
        return this.panjang;
    }
    double getLebar() {
        return this.lebar;
    }
    double getTinggi() {
        return this.tinggi;
    }
    
    public double hitungBalok() {
        return 0.0;
    }
}
